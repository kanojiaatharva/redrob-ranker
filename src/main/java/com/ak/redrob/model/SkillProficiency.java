package com.ak.redrob.model;

/**
 * Represents the proficiency level of a skill.
 */
public enum SkillProficiency {

    /**
     * Basic understanding.
     */
    BEGINNER,

    /**
     * Practical working knowledge.
     */
    INTERMEDIATE,

    /**
     * Strong hands-on expertise.
     */
    ADVANCED,

    /**
     * Expert-level proficiency.
     */
    EXPERT

}